public class Calculadora {
    public Float somar(Float nro1, Float nro2){
        return nro1 + nro2;
    }
    
    public Float subtrair(Float nro1, Float nro2){
        return nro1 - nro2;
    }
    
    public Float multiplicar(Float nro1, Float nro2){
        return nro1 * nro2;
    }
    
    public Float dividir(Float nro1, Float nro2){
        return nro1 / nro2;
    }
}
